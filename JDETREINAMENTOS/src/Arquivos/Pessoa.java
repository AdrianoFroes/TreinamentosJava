package Arquivos;

public class Pessoa {
	
	private String nome;
	
	private String email;
	
	private int indade;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getIndade() {
		return indade;
	}

	public void setIndade(int indade) {
		this.indade = indade;
	}
	
	
	

}
